				MOAManager is a high quality, visual, open source tool to create,
				runing, data extraction, and edit scripts files compatible with
				Framework MOA. <br>
				<br> It is for users and developers wanting to create databases,
				search, and edit data. It uses a simplify interface, and you don't
				need to learn complicated Framework MOA commands. <br>
				<br> Controls and wizards are available for users to:<br>

				<ul>
					<li>Create and compact files and scripts</li>
					<li>Create, edit, delete files and scripts</li>
					<li>Wizards for generate scripts</li>
					<li>Create, delete and restore backups</li>
					<li>Data extraction, transformation and loading from results</li>
					<li>Running and closed process for tasks</li>
					<li>Parallelism or serial execution of scripts in the Framework MOA</li>
					<li>Pause, finish and restart tasks</li>
					<li>Reporting detail of executions tasks</li>
					<li>Export data to CSV, JSON, LaTeX and HTML files</li>
					<li>Allows multiple MOA versions send by upload</li>
					<li>Add, delete and download of real datasets files</li>
					<li>Add, delete and restore files of trash</li>
					<li>Allows segmenting the workspace of users in disk drives
						differents</li>
					<li>Allows difference disk drives for running and data storage</li>
					<li>Allows move user workspace to another disc drive</li>
					<li>Block and delete users</li>
					<li>Execute instructions SQL</li>
					<li>Execute commands Shell</li>
				</ul>

